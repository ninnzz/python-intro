# List
fruits = ['apple', 'banana', 'orange', 'mango', 'kiwi', 'grapes', 'melon', 'dragon fruit']

# Accessing the list
print(fruits[0])
print(fruits[1])

# Wrong index
#print(fruits[20])

# Checking the length of the list
print(len(fruits))

# Advance access
print(fruits[-1])
print(fruits[2:4])



# Dictionary
student = {
	'name': 'Ninz',
	'age': 16,
	'gender': 'male',
	'hobbies': ['frisbee', 'music', 'astronomy']
}


# Accessing the property
print(student['name'])
